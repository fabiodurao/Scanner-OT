export const getSiteStats = (siteId: string): SiteStats => {
  const siteEquipment = mockEquipment.filter(eq => eq.site_id === siteId);
  const equipmentIds = siteEquipment.map(eq => eq.id);
  const siteVariables = mockVariables.filter(v => equipmentIds.includes(v.equipment_id));

  return {
    total_equipment: siteEquipment.length,
    total_variables: siteVariables.length,
    confirmed_variables: siteVariables.filter(v => v.learning_state === 'confirmed').length,
    published_variables: siteVariables.filter(v => v.learning_state === 'published').length,
    unknown_variables: siteVariables.filter(v => v.learning_state === 'unknown').length,
    hypothesis_variables: siteVariables.filter(v => v.learning_state === 'hypothesis').length,
  };
};